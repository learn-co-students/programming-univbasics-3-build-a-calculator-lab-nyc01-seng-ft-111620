def addition(num1, num2)
  num = num1 + num2
  return num
end

def subtraction(num1, num2)
  num = num1 - num2
  return num
end

def division(num1, num2)
  num = num1 / num2
  return num
end

def multiplication(num1, num2)
  num = num2 * num1
  return num
end

def modulo(num1, num2)
  num = num1 % num2
  return num
end

def square_root(num)
  root = Math.sqrt(num)
  return root
end
